# we will demonstrate the operational in python
a = 10
b = 5

# simple math operation
print((a + b), (a - b), (a * b), (a / b), (a % b))

# for the power of with can use **
print('The quadratic value', (a ** b))

# logic operator comparison
print((a > b), (a < b), ((a + b) > 10 and (a * b) <= 0))
print((a > b) or (a * b))
