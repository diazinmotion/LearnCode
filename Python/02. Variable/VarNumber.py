from math import *

# integer
number = 1
print(number)

# float
number = 3.1415
print(number)

# simple arithmetic
print(number + 12)
print(10 * (2 + 5))
print(7 % 3)
print(pow(2, 8))
print(abs(-230))
print(max(100, 200))
print(min(100, 200))
print(round(2.63))
print(floor(4.728))
print(ceil(4.728))
print(sqrt(81))

# convert number to string
age = 20
print("Hello i'm "+ str(age) +" years old")